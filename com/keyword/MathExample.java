package com.keyword;

public class MathExample {
    public static void main(String[] args) {
        System.out.println("Maximum : "+Math.max(4,6));
        System.out.println("Minimum : "+Math.min(8,2));
        System.out.println("Squareroot : "+Math.sqrt(4));
        System.out.println("Absolute value : "+Math.abs(-3.9));
        System.out.println("Random value : "+Math.random());
    }
}
